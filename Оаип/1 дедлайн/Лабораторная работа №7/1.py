direction = input ("Введите направление (left,right,straight,back):").lower()
if direction == "left":
 print("Иду налево")
elif direction == "right":
 print("Иду направо")
elif direction == "straight":
 print("Иду прямо")
elif direction == "back":
 print ("Иду назад")
else:
 print ("Неправильное направление")

